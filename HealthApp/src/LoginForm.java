    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String username = txtUsername.getText();
        String password = new String(txtPassword.getPassword()); // Get password as a string

        User user = userManager.loginUser (username, password);
        if (user != null) {
            if (username.equals("admin")) {
                JOptionPane.showMessageDialog(null, "Admin logged in successfully.");
                dispose(); // Close LoginForm
                new AdminDashboardForm(userManager).setVisible(true); // Open Admin Dashboard
            } else {
                JOptionPane.showMessageDialog(null, "You have successfully logged in.");
                dispose(); // Close LoginForm
                new UserProfileForm(user, userManager).setVisible(true); // Open User Profile Form
            }
        } else {
            JOptionPane.showMessageDialog(null, "Invalid credentials. Please try again.");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

